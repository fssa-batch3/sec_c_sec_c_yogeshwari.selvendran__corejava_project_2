package com.fssa.liveon.util;

public class Logger {
	/**
	 * First define the debug method to log debug messages
	 * 
	 * @param obj
	 */

	/**
	 * Second define the info method to log informational messages
	 * 
	 * @param obj
	 */
	public static void info(Object obj) {
		/**
		 * Print the provided object to the console as an info message
		 */
		System.out.println(obj);
	}
}
